const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const userSchema = new Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    username: {
        type: String,
        unique: true,
        required: true,
    },
    password: {
        type: String,
        minlength: 6,
        required: true,
    },
    role: {
        type: String,
        default: "Basic",
        required: true,
    },
    alerts: {
        newProduct: { type: Boolean, default: false },
        priceChange: { type: Boolean, default: false },
        promotion: { type: Boolean, default: false }
    }
});

const User = mongoose.model('User', userSchema);
module.exports = User;
